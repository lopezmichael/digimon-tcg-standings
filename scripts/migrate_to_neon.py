# Data migration
# ---------------------------------------------------------------------------


def migrate_table(duck_conn, pg_conn, table, dry_run=False):
    """Migrate a single table from DuckDB to Postgres.

    Returns the number of rows migrated (or that would be migrated in dry-run).
    Raises on error so the caller can handle it.
    """
    # Read all rows from DuckDB
    result = duck_conn.execute(f"SELECT * FROM {table}")
    columns = [desc[0] for desc in result.description]
    rows = result.fetchall()

    if not rows:
        print(f"  {table}: skipped (0 rows)")
        return 0

    row_count = len(rows)

    if dry_run:
        print(f"  {table}: {row_count} rows (would migrate)")
        return row_count

    cur = pg_conn.cursor()

    try:
        # Truncate existing data (CASCADE to handle FK references)
        cur.execute(f"TRUNCATE TABLE {table} CASCADE")

        # Build INSERT template
        cols_str = ", ".join(columns)
        template = f"({', '.join(['%s'] * len(columns))})"

        # Convert rows for Postgres compatibility
        converted_rows = [convert_row(row) for row in rows]

        # Bulk insert using execute_values for speed
        insert_sql = f"INSERT INTO {table} ({cols_str}) VALUES %s"
        execute_values(cur, insert_sql, converted_rows, template=template, page_size=1000)

        pg_conn.commit()
        print(f"  {table}: {row_count} rows migrated")
        return row_count

    except Exception:
        pg_conn.rollback()
        raise
    finally:
        cur.close()


def reset_sequences(pg_conn, dry_run=False):
    """Reset identity sequences for auto-increment tables to MAX(id) + 1."""
    print("\nResetting identity sequences...")
    cur = pg_conn.cursor()

    for table, id_col in IDENTITY_TABLES:
        sql = (
            f"SELECT setval("
            f"pg_get_serial_sequence('{table}', '{id_col}'), "
            f"COALESCE((SELECT MAX({id_col}) FROM {table}), 0) + 1, "
            f"false)"
        )

        if dry_run:
            print(f"  {table}.{id_col}: would reset sequence")
            continue

        try:
            cur.execute(sql)
            # Fetch the new sequence value for reporting
            new_val = cur.fetchone()[0]
            pg_conn.commit()
            print(f"  {table}.{id_col}: sequence set to {new_val}")
        except Exception as e:
            pg_conn.rollback()
            err_msg = str(e).strip().split("\n")[0]
            print(f"  {table}.{id_col}: ERROR - {err_msg[:100]}")

    cur.close()


def print_summary(duck_conn, pg_conn, dry_run=False):
    """Print row counts from both databases and flag mismatches."""
    print("\n" + "=" * 65)
    print("MIGRATION SUMMARY")
    print("=" * 65)
    print(f"{'Table':<30} {'DuckDB':>8} {'Postgres':>8} {'Status':>10}")
    print("-" * 65)

    all_match = True

    for table in TABLES:
        # DuckDB count
        try:
            duck_count = duck_conn.execute(f"SELECT COUNT(*) FROM {table}").fetchone()[0]
        except Exception:
            duck_count = "ERR"

        # Postgres count
        if dry_run:
            pg_count = "N/A"
            status = "dry-run"
        else:
            try:
                cur = pg_conn.cursor()
                cur.execute(f"SELECT COUNT(*) FROM {table}")
                pg_count = cur.fetchone()[0]
                cur.close()
            except Exception:
                pg_count = "ERR"

            if isinstance(duck_count, int) and isinstance(pg_count, int):
                if duck_count == pg_count:
                    status = "OK"
                else:
                    status = "MISMATCH"
                    all_match = False
            else:
                status = "ERROR"
                all_match = False

        print(f"  {table:<28} {str(duck_count):>8} {str(pg_count):>8} {status:>10}")

    print("-" * 65)

    if dry_run:
        print("Mode: DRY RUN (no data was written)")
    elif all_match:
        print("All tables match. Migration successful!")
    else:
        print("WARNING: Some tables have mismatches. Check errors above.")

    print("=" * 65)


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------


def main():
    parser = argparse.ArgumentParser(
        description="Migrate data from local DuckDB to Neon PostgreSQL."
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Preview what would be migrated without writing any data.",
    )
    parser.add_argument(
        "--schema-only",
        action="store_true",
        help="Run the Postgres schema SQL file on Neon before migrating data.",
    )
    args = parser.parse_args()

    # Load .env
    load_dotenv()

    print("=" * 65)
    print("DigiLab - DuckDB to Neon PostgreSQL Migration")
    print("=" * 65)

    if args.dry_run:
        print("MODE: DRY RUN (no changes will be made)")

    # -----------------------------------------------------------------------
    # Schema-only mode
    # -----------------------------------------------------------------------
    if args.schema_only:
        if args.dry_run:
            # Just show schema statements, no Postgres connection needed
            run_schema(None, dry_run=True)
            return

        print("\nConnecting to Neon Postgres...")
        pg_conn = connect_neon()
        print("  Connected.")

        run_schema(pg_conn, dry_run=False)
        pg_conn.close()
        print("\nDone. Schema applied to Neon.")
        return

    # -----------------------------------------------------------------------
    # Data migration mode
    # -----------------------------------------------------------------------
    print("\n[1/5] Connecting to local DuckDB...")
    duck_conn = connect_duckdb()
    print(f"  Connected: {Path(LOCAL_DB).absolute()}")

    pg_conn = None
    if not args.dry_run:
        print("\n[2/5] Connecting to Neon Postgres...")
        pg_conn = connect_neon()
        print("  Connected.")
    else:
        print("\n[2/5] Skipping Neon connection (dry run)")

    # Migrate each table
    print(f"\n[3/5] Migrating {len(TABLES)} tables...")
    total_rows = 0
    errors = []

    for table in TABLES:
        try:
            count = migrate_table(duck_conn, pg_conn, table, dry_run=args.dry_run)
            total_rows += count
        except Exception as e:
            err_msg = str(e).strip().split("\n")[0]
            print(f"  {table}: ERROR - {err_msg[:120]}")
            errors.append((table, err_msg))

    print(f"\n  Total: {total_rows} rows {'would be ' if args.dry_run else ''}migrated")
    if errors:
        print(f"  Errors: {len(errors)} table(s) failed")

    # Reset identity sequences
    if not args.dry_run:
        print("\n[4/5] Resetting identity sequences...")
        reset_sequences(pg_conn, dry_run=args.dry_run)
    else:
        print("\n[4/5] Resetting identity sequences...")
        reset_sequences(None, dry_run=True)

    # Summary
    print("\n[5/5] Verifying migration...")
    print_summary(duck_conn, pg_conn, dry_run=args.dry_run)

    # Cleanup
    duck_conn.close()
    if pg_conn:
        pg_conn.close()


if __name__ == "__main__":
    main()
