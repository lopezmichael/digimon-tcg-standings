"""
Migrate data from local DuckDB to Neon PostgreSQL.

Reads all tables from data/local.duckdb and inserts them into a Neon
Postgres database, respecting foreign key order (parents before children).

Usage:
    python scripts/migrate_to_neon.py                    # migrate data
    python scripts/migrate_to_neon.py --dry-run           # preview only
    python scripts/migrate_to_neon.py --schema-only       # create tables first
    python scripts/migrate_to_neon.py --schema-only --dry-run  # show schema SQL

Prerequisites:
    pip install duckdb psycopg2-binary python-dotenv

Environment variables (.env):
    NEON_HOST          - Neon Postgres hostname (required)
    NEON_DATABASE      - Database name (default: "neondb")
    NEON_USER          - Postgres user (required)
    NEON_PASSWORD      - Postgres password (required)
"""

import os
import sys
import argparse
import duckdb
import psycopg2
from psycopg2.extras import execute_values
from pathlib import Path
from dotenv import load_dotenv
from datetime import date, datetime
from decimal import Decimal

# ---------------------------------------------------------------------------
# Configuration
# ---------------------------------------------------------------------------

LOCAL_DB = "data/local.duckdb"
SCHEMA_FILE = "db/schema.sql"

# Tables in FK-safe order (parents before children)
TABLES = [
    "scenes",
    "stores",
    "store_schedules",
    "formats",
    "cards",
    "players",
    "deck_archetypes",
    "archetype_cards",
    "deck_requests",
    "tournaments",
    "results",
    "matches",
    "ingestion_log",
    "player_ratings_cache",
    "store_ratings_cache",
    "rating_snapshots",
    "limitless_deck_map",
    "limitless_sync_state",
    "admin_users",
]

# Tables that have auto-increment (GENERATED BY DEFAULT AS IDENTITY) columns
# Format: (table_name, id_column_name)
IDENTITY_TABLES = [
    ("scenes", "scene_id"),
    ("stores", "store_id"),
    ("store_schedules", "schedule_id"),
    ("players", "player_id"),
    ("deck_archetypes", "archetype_id"),
    ("deck_requests", "request_id"),
    ("tournaments", "tournament_id"),
    ("results", "result_id"),
    ("matches", "match_id"),
    ("ingestion_log", "log_id"),
    ("admin_users", "user_id"),
]

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def connect_duckdb():
    """Connect to local DuckDB in read-only mode."""
    db_path = Path(LOCAL_DB)
    if not db_path.exists():
        print(f"Error: Local database not found at {db_path.absolute()}")
        sys.exit(1)
    return duckdb.connect(str(db_path), read_only=True)


def connect_neon():
    """Connect to Neon Postgres using env vars."""
    host = os.getenv("NEON_HOST")
    database = os.getenv("NEON_DATABASE", "neondb")
    user = os.getenv("NEON_USER")
    password = os.getenv("NEON_PASSWORD")

    missing = []
    if not host:
        missing.append("NEON_HOST")
    if not user:
        missing.append("NEON_USER")
    if not password:
        missing.append("NEON_PASSWORD")
    if missing:
        print(f"Error: Missing required env vars: {', '.join(missing)}")
        print("Set them in your .env file.")
        sys.exit(1)

    return psycopg2.connect(
        host=host,
        port=5432,
        dbname=database,
        user=user,
        password=password,
        sslmode="require",
    )


def convert_row(row):
    """Convert a single row's values for Postgres compatibility.

    DuckDB may return types that psycopg2 doesn't handle automatically:
    - Decimal -> float (for cleaner insert; Postgres casts back)
    - Other types (bool, date, datetime, str, int, None) pass through fine.
    """
    converted = []
    for val in row:
        if isinstance(val, Decimal):
            converted.append(float(val))
        else:
            converted.append(val)
    return tuple(converted)


# ---------------------------------------------------------------------------
# Schema operations
# ---------------------------------------------------------------------------


def run_schema(pg_conn, dry_run=False):
    """Execute the Postgres schema SQL file on Neon."""
    schema_path = Path(SCHEMA_FILE)
    if not schema_path.exists():
        print(f"Error: Schema file not found at {schema_path.absolute()}")
        sys.exit(1)

    with open(schema_path, "r", encoding="utf-8") as f:
        content = f.read()

    # Split on semicolons, filter out empty/comment-only statements
    statements = content.split(";")
    valid_stmts = []
    for stmt in statements:
        cleaned = stmt.strip()
        # Remove pure comment lines to check if anything real remains
        lines = [
            line
            for line in cleaned.split("\n")
            if line.strip() and not line.strip().startswith("--")
        ]
        if lines:
            valid_stmts.append(cleaned)

    print(f"\nSchema file: {SCHEMA_FILE}")
    print(f"Statements to execute: {len(valid_stmts)}")

    if dry_run:
        print("\n[DRY RUN] Schema statements that would be executed:\n")
        for i, stmt in enumerate(valid_stmts, 1):
            # Show first line of each statement for readability
            first_line = stmt.split("\n")[0].strip()
            # Remove leading comment lines to find the actual SQL
            for line in stmt.split("\n"):
                stripped = line.strip()
                if stripped and not stripped.startswith("--"):
                    first_line = stripped
                    break
            print(f"  {i:3d}. {first_line[:100]}")
        print(f"\n[DRY RUN] Would execute {len(valid_stmts)} statements. No changes made.")
        return

    cur = pg_conn.cursor()
    success = 0
    errors = 0

    for i, stmt in enumerate(valid_stmts, 1):
        try:
            cur.execute(stmt)
            pg_conn.commit()
            success += 1
        except Exception as e:
            pg_conn.rollback()
            err_msg = str(e).strip().split("\n")[0]
            # Suppress "already exists" noise
            if "already exists" in err_msg.lower():
                success += 1
            else:
                errors += 1
                print(f"  Warning (stmt {i}): {err_msg[:120]}")

    cur.close()
    print(f"Schema applied: {success} succeeded, {errors} errors")


